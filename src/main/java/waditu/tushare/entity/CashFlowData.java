package waditu.tushare.entity;

public class CashFlowData {

	/** 代码 */
    public String code;
    /** 名称 */
    public String name;
    /** 经营现金净流量对销售收入比率 */
    public double cfSales;
    /** 资产的经营现金流量回报率 */
    public double rateOfReturn;
    /** 经营现金净流量与净利润的比率 */
    public double cfNm;
    /** 经营现金净流量对负债比率 */
    public double cfLiabilities;
    /** 现金流量比率 */
    public double cashFlowRatio;
	
}
